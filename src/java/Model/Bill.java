package Model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Bill {
    private String ticketid,clientid;
    private float amount;
    private Date date;
    public Bill() {
    }

    public Bill(String ticketid, String clientid, float amount, Date date) {
        this.ticketid = ticketid;
        this.clientid = clientid;
        this.amount = amount;
        this.date = date;
    }

    public String getTicketid() {
        return ticketid;
    }

    public void setTicketid(String ticketid) {
        this.ticketid = ticketid;
    }

    public String getClientid() {
        return clientid;
    }

    public void setClientid(String clientid) {
        this.clientid = clientid;
    }

    public float getAmount() {
        return amount;
    }

    public void setAmount(float amount) {
        this.amount = amount;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    
    
    
}
